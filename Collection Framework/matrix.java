
public class matrix {
    public static void main(String [] args){
        
        int n = 3, m = 3;
        // create 2d array
        int [][] mat = new int[n][m];
        
        // initalize with 0
        for(int i = 0; i<n ; i++){
            for(int j = 0; j<m; j++){
                mat[i][j] = 0;
            }
        }

        
    }    
}
